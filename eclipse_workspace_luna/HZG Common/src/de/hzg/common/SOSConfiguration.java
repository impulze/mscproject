package de.hzg.common;

public class SOSConfiguration {
	String procedureIdentifierPrefix;
	String offeringIdentifierPrefix;
	String offeringName;
	String featureOfInterestIdentifierPrefix;
	String featureOfInterestName;
	String observablePropertyIdentifierPrefix;
	
	public String getProcedureIdentifierPrefix() {
		return procedureIdentifierPrefix;
	}

	public void setProcedureIdentifierPrefix(String procedureIdentifierPrefix) {
		this.procedureIdentifierPrefix = procedureIdentifierPrefix;
	}

	public String getOfferingIdentifierPrefix() {
		return offeringIdentifierPrefix;
	}

	public void setOfferingIdentifierPrefix(String offeringIdentifierPrefix) {
		this.offeringIdentifierPrefix = offeringIdentifierPrefix;
	}

	public String getOfferingName() {
		return offeringName;
	}

	public void setOfferingName(String offeringName) {
		this.offeringName = offeringName;
	}

	public String getFeatureOfInterestIdentifierPrefix() {
		return featureOfInterestIdentifierPrefix;
	}

	public void setFeatureOfInterestIdentifierPrefix(String featureOfInterestIdentifierPrefix) {
		this.featureOfInterestIdentifierPrefix = featureOfInterestIdentifierPrefix;
	}

	public String getFeatureOfInterestName() {
		return featureOfInterestName;
	}

	public void setFeatureOfInterestName(String featureOfInterestName) {
		this.featureOfInterestName = featureOfInterestName;
	}

	public String getObservablePropertyIdentifierPrefix() {
		return observablePropertyIdentifierPrefix;
	}

	public void setObservablePropertyIdentifierPrefix(String observablePropertyIdentifierPrefix) {
		this.observablePropertyIdentifierPrefix = observablePropertyIdentifierPrefix;
	}
}